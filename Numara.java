public class Numara {
  public static void main(String[] args) {
    // Numaralar tırnak içerisine konmak zorunda değildir.
    System.out.println(3.5); // Çıktı: 3.5
    System.out.println(5); // Çıktı: 5
    System.out.println(256); // Çıktı: 256
    
    // Numara yazdırılırken toplama çıkarma işlemi yapılabilir
    System.out.println(5 - 3); // Çıktı: 2
    
    // Yazı ile beraber numara da yazdırılabilir
    System.out.println("Öğrenci numaranız: " + 27); // Çıktı: Öğrenci numaranız: 27
  }
}
