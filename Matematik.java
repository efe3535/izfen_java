public class Matematik {
  static int a = 5, b = 7;

  public static void main(String[] args) {
    Math.min(a, b); // a ve b sayılarından küçük olanı döndürür
    Math.max(a, b); // a ve b sayılarından büyük olanı döndürür
    Math.sqrt(256); // square root işlemi (karekök) = √256 = 16
    Math.abs(-5.355); // mutlak değer işlemi
    Math.random(); // 0 ile 1 arasında rastgele bir değer oluşturur.
  }
}
